"""
app.py  —  CarVal Flask Application
====================================
Serves the car-price prediction UI and /predict API endpoint.

Requires (generated by train_model.py):
    models/gradient_boosting_model.pkl
    models/preprocessor.pkl
    models/label_encoder_model.pkl
    models/model_classes.json
"""

import json
import os
import pickle

import numpy as np
import pandas as pd
from flask import Flask, jsonify, render_template, request

BASE_DIR     = os.path.dirname(os.path.abspath(__file__))
MODELS_DIR   = os.path.join(BASE_DIR, "models")
CURRENT_YEAR = 2024

ONEHOT_COLS = ["seller_type", "fuel_type", "transmission_type"]
LABEL_COL   = "model"

def _load(filename):
    path = os.path.join(MODELS_DIR, filename)
    if os.path.exists(path):
        with open(path, "rb") as f:
            return pickle.load(f)
    return None

gb_model     = _load("gradient_boosting_model.pkl")
preprocessor = _load("preprocessor.pkl")
label_enc    = _load("label_encoder_model.pkl")

model_classes_path = os.path.join(MODELS_DIR, "model_classes.json")
if os.path.exists(model_classes_path):
    with open(model_classes_path) as f:
        CAR_MODELS = json.load(f)
else:
    CAR_MODELS = ["Alto", "Swift", "i20", "Creta", "Fortuner"]

MODELS_LOADED = all([gb_model, preprocessor, label_enc])

SELLER_TYPES  = ["Dealer", "Individual", "Trustmark Dealer"]
FUEL_TYPES    = ["Diesel", "Petrol", "CNG", "LPG", "Electric"]
TRANSMISSIONS = ["Manual", "Automatic"]

app = Flask(__name__)

@app.route("/")
def index():
    return render_template(
        "index.html",
        car_models=sorted(CAR_MODELS),
        seller_types=SELLER_TYPES,
        fuel_types=FUEL_TYPES,
        transmissions=TRANSMISSIONS,
        current_year=CURRENT_YEAR,
        models_loaded=MODELS_LOADED,
    )

@app.route("/predict", methods=["POST"])
def predict():
    try:
        data = request.get_json(force=True)

        car_model    = data["car_model"]
        vehicle_age  = int(data["vehicle_age"])
        km_driven    = float(data["km_driven"])
        seller_type  = data["seller_type"]
        fuel_type    = data["fuel_type"]
        transmission = data["transmission_type"]
        mileage      = float(data["mileage"])
        engine       = float(data["engine"])
        max_power    = float(data["max_power"])
        seats        = int(data["seats"])

        row = pd.DataFrame([{
            "model":             car_model,
            "vehicle_age":       vehicle_age,
            "km_driven":         km_driven,
            "seller_type":       seller_type,
            "fuel_type":         fuel_type,
            "transmission_type": transmission,
            "mileage":           mileage,
            "engine":            engine,
            "max_power":         max_power,
            "seats":             seats,
        }])

        if MODELS_LOADED:
            row["model"] = label_enc.transform(row["model"])
            X_transformed = preprocessor.transform(row)
            price = float(gb_model.predict(X_transformed)[0])
            price = max(0, round(price, 2))
            source = "Gradient Boosting (CV R\u00b2=0.90, Test R\u00b2=0.94)"
        else:
            price = max(
                50000,
                900000
                - vehicle_age * 45000
                - km_driven * 0.8
                + (40000 if transmission == "Automatic" else 0)
                + (25000 if fuel_type == "Diesel" else 0)
                + max_power * 1200
                - (seats - 5) * 10000,
            )
            price = round(price, 2)
            source = "demo (model files not found — run train_model.py)"

        return jsonify({"success": True, "price": price, "source": source})

    except KeyError as e:
        return jsonify({"success": False, "error": f"Missing field: {e}"}), 400
    except Exception as e:
        return jsonify({"success": False, "error": str(e)}), 500

if __name__ == "__main__":
    if not MODELS_LOADED:
        print("\n\u26a0  Model files not found in ./models/ \u2014 running in demo mode.")
        print("   Run:  python train_model.py --data path/to/cardekho_imputated.csv\n")
    app.run(debug=True)
